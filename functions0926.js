function generate_parenting_type() {//判断每个分量表的水平，获得报告名称
    // 定义评分标准
    const standards = {
        Control: 50,
        Rules: 50,
        Commu: 50,
        Support: 50,
    };

    // 计算每个量表的总分
    const scores = {
        Control: SUM('Control'),
        Rules: SUM('Rules'),
        Commu: SUM('Commu'),
        Support: SUM('Support')
    };

    // 定义水平标签
    const levels = {
        '高于': '高',
        '低于': '低'
    };

    // 判断每个量表的水平
    const evaluateLevel = (score, standard) => score >= standard ? levels['高于'] : levels['低于'];
    const levelsResult = {
        Control: evaluateLevel(scores.Control, standards.Control),
        Rules: evaluateLevel(scores.Rules, standards.Rules),
        Commu: evaluateLevel(scores.Commu, standards.Commu),
        Support: evaluateLevel(scores.Support, standards.Support)
    };

    //组合报告名称(各维度水平)
    const leves_combine = `${levelsResult.Control}${levelsResult.Rules}${levelsResult.Commu}${levelsResult.Support}`;
        
    const reportInfo = parenting_map.find(config => config.description === leves_combine);
    const parenting_type = reportInfo ? reportInfo.type : "未知类型：未匹配到任何已知组合。";
    return { type: parenting_type, description: leves_combine };
}

