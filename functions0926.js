function addRespFromButtonScale(data, scale_name, var_i = 'i') {
    // 确保 data 对象中有 button_pressed 属性
    if (data.button_pressed) {
    // 将字符串选项转换为数字
        data.response = parseInt(data.button_pressed) + 1 // raw: 0, 1, 2, ...
    } else {
        console.error('No button pressed data available.');
    }

    // compute variables from button-plugin response (for likert scale)
    data.scale = scale_name
    data.varname = scale_name + data[var_i]
    
    // 检查是否有反向计分
    if (data.Reversescoring === 1) {
        // 应用反向计分逻辑
        data.response = 6 - data.response; // 反向计分
    }
}


function SUM(scale_name) {
    // 获取指定量表的数据
    var df = jsPsych.data.get().filter({ scale: scale_name }).values();
    var sum = 0;

    df.forEach(function(trial) {
        // 直接累加 response 值
        sum += trial.response; // 反向计分后的结果
    });

    // 定义每个量表的总分
    const full_score = {
        Control: 60,
        Rules: 20,
        Commu: 40,
        Support: 30
    };

    // 计算百分制得分
    const full_score_scale = full_score[scale_name];
    const percentage = (sum / full_score_scale)*100;
    const integerPercentage = Math.round(percentage); // 保留整数

    return integerPercentage; // 返回总分
}

